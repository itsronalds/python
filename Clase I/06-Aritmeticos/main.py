'''
Operadores aritméticos
'''

# Suma
print(10 + 10)

# Resta
print(20 - 10)

# Multiplicación
print(2 * 10)

# División
print(10 / 2)

# División inexacta con el operador módulo: %
print(10 % 3)

# División que devuelve siempre un valor entero
print(10 // 3)

# Exponencial
print(2 ** 3)

print('--------------------------------------------------------------------------------')

# Operadores aritméticos en Strings
print('Hello' + ' World')
print('Hello World ' * 2)

print('--------------------------------------------------------------------------------')

# Ejemplo 1: Operación matemática
print(2 ** 3 + 3 - 7 / 1 // 4)

# Desglosar operación matemática
print(2 ** 3)
print((7 / 1) // 4)
print(3)

# Ejemplo 2: Operación matemática
print((10 + 1) ** 2 - 3 + 4/2 // 1)

print((10 + 1) ** 2)
print(4/2 // 1)
print(-3)

